# For loop
print("For loop:")
for i in range(5):
    print(f"Number {i}")

# While loop
print("\nWhile loop:")
count = 0
while count < 5:
    print(f"Count is {count}")
    count += 1

# For loop with else 
print("For loop:")
for i in range(5):
    print(f"Number {i}")
else :
    print("Loop terminated")
 
# While loop with else statement
print("\nWhile loop:")
count = 0
while count < 5:
    print(f"Count is {count}")
    count += 1
else :
    print("Loop terminated")
